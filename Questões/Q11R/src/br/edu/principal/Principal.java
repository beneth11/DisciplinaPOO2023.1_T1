package br.edu.principal;

public class Principal {

	public static void main(String[] args) {
		int num = 2;
		
		double raizQ = Math.sqrt(num);
		
		double raizC = Math.cbrt(num);
		
		double quadnum = 2^2;
		
		double cubnum = 2^2;
		
		
		System.out.println("O número é: " + num);
		
		System.out.println("A raíz quadrada é de: " + quadnum);
		
		System.out.println("A raíz cúbica é de: " + cubnum);
		
		System.out.println("O valor do número ao quadrado é " + raizQ);
		
		System.out.println("O valor do número ao cubo é: " + raizC);
		
	}

}
