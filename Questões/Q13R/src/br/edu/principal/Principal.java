package br.edu.principal;

public class Principal {

	public static void main(String[] args) {
		int pes = 12;
		int polegadas = pes * 12;
		int jardas = pes / 3;
		double milhas = jardas / 1760;
		
		System.out.println("O número convertido em polegadas é de: " + polegadas + " polegadas.");
		System.out.println("O número convertido em jardas é de: " + jardas + " jardas.");
		System.out.println("O número convertido em milhas é de: " + milhas + " milhas.");
		

	}

}
